alter table public.clientes enable row level security;

-- Users may only see rows that belong to their organization.
drop policy if exists clientes_select_own_org on public.clientes;
create policy clientes_select_own_org
on public.clientes
for select
using (organization_id = auth.uid());

-- Users may only insert rows for their own organization.
drop policy if exists clientes_insert_own_org on public.clientes;
create policy clientes_insert_own_org
on public.clientes
for insert
with check (organization_id = auth.uid());

-- Users may only update rows that belong to their organization.
drop policy if exists clientes_update_own_org on public.clientes;
create policy clientes_update_own_org
on public.clientes
for update
using (organization_id = auth.uid())
with check (organization_id = auth.uid());

-- Users may only delete rows that belong to their organization.
drop policy if exists clientes_delete_own_org on public.clientes;
create policy clientes_delete_own_org
on public.clientes
for delete
using (organization_id = auth.uid());

