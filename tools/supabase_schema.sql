create index if not exists idx_trades_status on public.trades(status);
create index if not exists idx_trades_created_at on public.trades(created_at desc);

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
    new.updated_at = timezone('utc', now());
    return new;
end;
$$;

drop trigger if exists trg_clientes_updated_at on public.clientes;
create trigger trg_clientes_updated_at
before update on public.clientes
for each row
execute function public.set_updated_at();

-- ---------------------------------------------------------------------------
-- Multi-tenancy: enforce organization_id = auth.uid() on every INSERT
-- ---------------------------------------------------------------------------

-- Trigger function: overwrites organization_id with the JWT caller's uid so that
-- even if the application omits the field (or passes a wrong value) the row is
-- always owned by the authenticated user.
create or replace function public.set_organization_id()
returns trigger
language plpgsql
security invoker
as $$
begin
    -- Reject inserts that arrive without an authenticated JWT.
    if auth.uid() is null then
        raise exception 'organization_id cannot be set: no authenticated user (auth.uid() is NULL)';
    end if;
    new.organization_id = auth.uid();
    return new;
end;
$$;

drop trigger if exists trg_clientes_set_organization_id on public.clientes;
create trigger trg_clientes_set_organization_id
before insert on public.clientes
for each row
execute function public.set_organization_id();

-- ---------------------------------------------------------------------------
-- Row-Level Security (RLS)
